#O for funciona até um número antes do fim.

print ('-' * 20)

for c in range (1, 7):
    print (c) #para contar números.
print ('fim da contagem.')

print ('-' * 20)

for c in range (6, 0, -1):
    print (c) #contagem regressiva.
print ('Acabei de contar.')

print ('-' * 20)

for c in range (0, 7, 2):
    print (c) #para contar de 2 em 2.
print ('fim.')

print ('-' * 20)